#include "libft.h"

void		*ft_memset(void *s, int c, size_t n)
{
	char	*str;

	if (!s)
		return (0);
	str = (char*)s;
	while (n > 0)
	{
		*str = (char)c;
		str++;
		n--;
	}
	return (s);
}
